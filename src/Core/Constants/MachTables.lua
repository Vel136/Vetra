--[[
    Empirical Mach/Cd tables for the standard G-series ballistic drag functions,
    plus the LerpMachTable helper that both the serial and parallel drag paths
    use to interpolate them.

    Separated from Constants so the data has a clear home and Constants does not
    become a dumping ground for large tables.  Constants requires this module and
    re-exports it as Constants.MACH_TABLES so callers that already have a
    Constants reference do not need a second require.

    Table format: each entry is { mach, cd }, sorted in ascending Mach order.
    This sort order is required by LerpMachTable's binary search — do not reorder.

