-- ─── Helper ───────────────────────────────────────────────────────────────────

--[[
    Binary-search + linear interpolation over a sorted { mach, cd } table.

    Works with both the built-in G-series tables above and user-supplied
    CustomMachTable values — any table of { {mach, cd}, ... } pairs sorted
    in ascending Mach order is valid input.

    Returns the interpolated Cd at the given Mach number. Clamps to the
    table's first/last entry outside the tabulated range (no extrapolation),
    matching standard ballistics library behaviour.
]]
function MachTables.Lerp(MachTable: { { number } }, Mach: number): number
	local n = #MachTable

	-- Clamp below table minimum
	if Mach <= MachTable[1][1] then
		return MachTable[1][2]
	end

	-- Clamp above table maximum
	if Mach >= MachTable[n][1] then
		return MachTable[n][2]
	end

	-- Binary search for the bracketing interval [lo, hi]
	local lo, hi = 1, n
	while hi - lo > 1 do
		local mid = math.floor((lo + hi) * 0.5)
		if MachTable[mid][1] <= Mach then
			lo = mid
		else
			hi = mid
		end
	end

	local m0, c0 = MachTable[lo][1], MachTable[lo][2]
	local m1, c1 = MachTable[hi][1], MachTable[hi][2]
	local t      = (Mach - m0) / (m1 - m0)
	return c0 + t * (c1 - c0)
end

